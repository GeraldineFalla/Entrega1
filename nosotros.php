      <section class="about container">
            <div class="about-txt">
                <h3> ¿Quienes somos?</h3>
                <p>
                Somos una empresa que apoya a los profesionales de la salud mental otorgando elementos caninos, felinos, equinos y lagomorfos para que se sean de ayuda en las terapias.
                </p>
                </div>

                <div class="about-txt">
                <h3>Historia</h3>
                <p>
                Heal Paws nace en 2023, al encontrar que los animales que se utilizaban como animales de terapia en Colombia no cumplían con 
                los requisitos básicos en entrenamiento, bienestar y protección. Ademas, al ser un campo relativamente nuevo en el país,
                los profesionales de salud mental no sabían a quien acudir cuando necesitan de la ayuda de un peludo de 4 patas para sus terapias. 
                Es por ello que Heal Paws supone ser una fuente confiable de información y promoción de las terapias asistidas con animales. 
                </p>
                </div>

                <div class="about-txt">
                <h3>Misión</h3>
                <p>
                Nuestra misión es la promoción de las terapias asistidas con animales en Colombia, ya que ha demostrado ser efectiva en la mayoría de casos; salvarguardando la integridad y bienestar de los animales.
                </p>
                </div>

                <div class="about-txt">
                <h3>Visión</h3>
                <p>
                Ser líderes en la promoción de la salud mental a través de terapias asistidas por animales, ofreciendo soluciones 
                y recursos de alta calidad que involucran a caninos, felinos, equinos y lagomorfos, y contribuyendo al bienestar emocional 
                y psicológico de las personas en todo el mundo.
                </p>
                </div>

                <div class="about-txt">
                <h3> ¿Cómo funciona?</h3>
                <p>
                Al contratar los servicios de Heal Paws, tendrás acceso a nuestro catálogo y precios de los elementos con los que contamos. 
                Podrás agendar una cita para conocer lo elementos disponibles y así escoger el que mejor se adapta a las necesidades de tu paciente. 
                Adicionalmente, tendrás acceso a asesoría con los entrenadores de nuestros elementos, tips y consejos para su cuidado. 
                </p>
                </div>

                <div class="about-txt">
                <h3>¿Y que hay de los derechos de los animales?</h3>
                <p>
                La seguridad y bienestar de nuestros animales es nuestra prioridad. Contamos con veterinarios especializados que día a día revisan la salud de cada uno de nuestros elementos. 
                Cada elemento tiene su horario de trabajo (menos de 5 horas al día) y cuentan con 2 días de descanso, donde socializan con otros animales de su raza. 
                Su alimentación es balanceada, cuentan con vacunas al día, pruebas virales, entre otros procedimientos propios de cada raza. 
                </p>
                </div>                    
              
